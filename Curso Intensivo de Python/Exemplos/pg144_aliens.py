# Criando 3 dicionários:
alien_0 = {'color':'green', 'pointes':5}
alien_1 = {'color':'yellow', 'points':5}
alien_2 = {'color':'red', 'points':15}

# Criando uma lista com os dicionários:
aliens = [alien_0, alien_1, alien_2]

# Criando o laço for:
print("Imprimindo os dicionários da lista.")
for alien in aliens:
    print(alien)

